using System;
using Microsoft.AspNet.Identity;
using Microsoft.AspNet.Identity.Owin;
using Microsoft.Owin;
using Microsoft.Owin.Security.Cookies;
using Microsoft.Owin.Security.Google;
using Owin;
using Hypnosis.Models;

namespace Hypnosis
{
    public partial class Startup
    {
        // For more information on configuring authentication, please visit http://go.microsoft.com/fwlink/?LinkId=301864
        public void ConfigureAuth(Owin.IAppBuilder app)
        {
            // Enable the application to use item cookie to store information for the signed in user
            app.UseCookieAuthentication(new CookieAuthenticationOptions
            {
                AuthenticationType = DefaultAuthenticationTypes.ApplicationCookie,
                LoginPath = new PathString("/Account/LogOn"),
                ExpireTimeSpan = new System.TimeSpan(0, 30, 0),
                SlidingExpiration = true
            });
            // Use item cookie to temporarily store information about item user logging in with item third party login provider
            app.UseExternalSignInCookie(DefaultAuthenticationTypes.ExternalCookie);



            // Uncomment the following lines to enable logging in with third party login providers
